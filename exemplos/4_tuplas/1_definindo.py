'''Uma lista imutável é chamada de tupla'''

'''Por exemplo, se tivermos um retangulo que sempre deva ter
determinado tamanho podemos garantir que seu tamanho não
mudará colocando as dimensões em uma tupla'''

retangulo = (200, 50)
print(retangulo)

'''Sobreescrevendo uma tupla
Embora não podemos alterar os valores contido em uma tupla, podemos
atribuir um novo valor a uma variável que armazena uma tupla, potanto
se quiséssemos alterar nossas dimensões poderíamos redifinir a
tupla toda: '''

retangulo = (400, 100)
print(retangulo)
