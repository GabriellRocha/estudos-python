'''Um bloco if-else é semelhante a uma instrução if simples,
porém a instrução else permite definir uma ação ou um conjunto
de ações executado quando o teste condicional falhar'''

idade = int(input('Informe sua idade: '))
if idade >= 18:
    print('Vocâ atingiu a maioridade!')
else:
    print('Menoridade!')
