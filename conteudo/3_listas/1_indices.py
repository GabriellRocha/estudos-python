'''Listas são sequencias ordenadas, portanto você pode acessar
qualquer elemento de uma lista informando a posição - ou índice-
do item ao interpretador Python'''

'''Nome de listas sempre no plural'''

frutas = ['banana', 'maça', 'laranja', 'melancia', 'abacaxi']
print(frutas[0])

print(frutas[-1])  # devolve o último elemento da lista
